// Orden de las operaciones 

let resultado;

// Multiplicación y división tienen prioridad sobre la suma y la resta
resultado = 20 + 30 * 4;

// Primer se realiza la multiplicación y luego la suma
resultado = (600 + 600) * 1.16;

//Incrementos 
resultado = 10;
//Incremento en 1 es = ++
//Incremento ++resultado es primero incrementa y luego muestra el resultado
resultado ++;
console.log(++resultado);

//Decremento en 1 es = --
//Decremento --resultado es primero decrementa y luego muestra el resultado
resultado --;
console.log(--resultado);

//Incremento en 3
resultado += 3;
console.log(resultado);